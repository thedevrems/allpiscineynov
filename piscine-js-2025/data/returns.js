// Définition de fonctions pour retourner des valeurs ou des propriétés

// Fonction qui retourne la valeur passée en argument
function id(value) {
    return value;
}

// Fonction qui retourne la longueur de l'élément passé en argument
// L'élément doit avoir une propriété 'length' (comme les tableaux ou les chaînes de caractères)
function getLength(item) {
    return item.length;
}