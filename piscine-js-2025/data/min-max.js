/**
 * Retourne le plus grand des deux nombres donnés.
 * 
 * @param {number} a - Premier nombre.
 * @param {number} b - Deuxième nombre.
 * @returns {number} - Le plus grand des deux nombres.
 */
function max(a, b) {
  return a > b ? a : b;
}

/**
* Retourne le plus petit des deux nombres donnés.
* 
* @param {number} a - Premier nombre.
* @param {number} b - Deuxième nombre.
* @returns {number} - Le plus petit des deux nombres.
*/
function min(a, b) {
  return a < b ? a : b;
}