// Définition de fonctions pour effectuer des opérations arithmétiques simples

// Fonction qui incrémente la valeur de x de 1
function more(x) {
    return x + 1;
}

// Fonction qui décrémente la valeur de x de 1
function less(x) {
    return x - 1;
}

// Fonction qui additionne deux valeurs x et y
function add(x, y) {
    return x + y;
}

// Fonction qui soustrait la valeur y de la valeur x
function sub(x, y) {
    return x - y;
}